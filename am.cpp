#include <iostream>

using namespace std;

int main() {
    int x = 7;                                                // declaration and initialisation
    cout << x+8 ;
    return 0;
}

/*
int x=7;
This line declares an integer variable named x and assigns the value 7 to the variable x.

cout << x + 8;
This line prints the result of adding 8 to the value of x (which is 7) to the standard output stream using the cout object. 
The expression x + 8 evaluates to 7 + 8 = 15, so 15 will be printed to the console.
*/