#include <iostream>

using namespace std;

int main() {
    int x;                                                 // declaration
    x = 7;                                                 // initialisation
    cout << x ;
    
    return 0;
}

/*
This C++ program declares an integer variable x, assigns it the value 7, and then prints the value of x to the standard output. 
The output of this program will be 7
*/


/*
int x;
This line declares an integer variable named x without initializing it. 
This means that the value of x is uninitialized and may contain garbage data.

x = 7;
This line assigns the value 7 to the variable x. Now x holds the value 7.

cout << x;
This line prints the value of the variable x to the standard output stream using the cout object. 
The value of x (which is 7) is printed to the console.
*/