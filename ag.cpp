#include <iostream>

using namespace std;

int main() {
    cout << "Har Har Mahadev" << endl << endl << endl << endl << endl << endl << endl << endl << endl << endl;
    cout << "Jai Shree Ram";
    return 0;
}

/*
This C++ program prints "Har Har Mahadev" followed by ten newline characters (resulting in ten blank lines), 
and then "Jai Shree Ram" without a newline. Finally, it terminates successfully.
*/



