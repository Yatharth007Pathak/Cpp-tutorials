#include <iostream>

using namespace std;

int main() {
    int x=8, y=4;
    cout << x+y << endl;
    cout << x-y << endl;
    cout << x*y << endl;
    cout << x/y << endl;
    return 0;
}

/*
int x=8, y=4;
This line declares two integer variables named x and y and assign the values 8 and 4 to the variables x and y respectively.

cout << x + y << endl;
This line prints the result of adding x and y together to the standard output stream using the cout object. 
The expression x + y evaluates to 8 + 4 = 12, so 12 will be printed to the console followed by a newline.

cout << x - y << endl;
This line prints the result of subtracting y from x to the standard output stream. 
The expression x - y evaluates to 8 - 4 = 4, so 4 will be printed to the console followed by a newline.

cout << x * y << endl;
This line prints the result of multiplying x and y together to the standard output stream. 
The expression x * y evaluates to 8 * 4 = 32, so 32 will be printed to the console followed by a newline.

cout << x / y << endl;
This line prints the result of dividing x by y to the standard output stream.
The expression x / y evaluates to 8 / 4 = 2, so 2 will be printed to the console followed by a newline.
*/