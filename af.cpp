#include <iostream>

using namespace std;

int main() {
    cout << "Har Har Mahadev";
    cout << endl;
    cout << endl;
    cout << "Jai Shree Ram";
    
    return 0;
}


/*
this C++ program prints "Har Har Mahadev" followed by two newlines (resulting in a blank line), 
and then "Jai Shree Ram" without a newline. Finally, it terminates successfully.
*/



/*
cout << "Har Har Mahadev";
This line outputs the string "Har Har Mahadev" to the standard output (usually the console) using the cout object.

cout << endl;
This line inserts a newline character into the output stream using the endl manipulator. As a result, it moves the output to the next line.

cout << endl;
Another endl is inserted, causing an additional newline character to be added to the output stream, resulting in a blank line.

cout << "Jai Shree Ram";
This line outputs the string "Jai Shree Ram" to the standard output without inserting a newline after it.
*/